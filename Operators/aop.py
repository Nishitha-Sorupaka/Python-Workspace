#Demonstrating all Arithmetic Operators
a=int(input("Enter a value:"))
b=int(input("Enter b value:"))
print("===================================")

print("A R I T H M E T I C   O P E R A T O R S")

print("-------------------------------------")
print("\t\tadd({},{})={}".format(a,b,a+b))
print("\t\tsub({},{})={}".format(a,b,a-b))
print("\t\tmul({},{})={}".format(a,b,a*b))
print("\t\tdiv({},{})={}".format(a,b,a/b))
print("\t\tFloor Div({},{})={}".format(a,b,a//b))
print("\t\tmod({},{})={}".format(a,b,a%b))
print("\t\texpo({},{})={}".format(a,b,a**b))
print("===================================")